package br.ce.wcaquino.entidades;

public class Calculadora {

    public Integer soma(Integer init, Integer aux) {
        return init + aux;
    }

    public Integer subtracao(Integer init, Integer aux) {
        return init - aux;
    }

    public Integer divisao(Integer init, Integer aux) {
        return init / aux;
    }

    public Integer multiplicacao(Integer init, Integer aux) {
        return init * aux;
    }
}
